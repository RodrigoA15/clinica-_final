     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $doctor = new Doctor();
        $doctor->nombre_doctor = $request->nombre_doctor;
        $doctor->apellido_doctor = $request->apellido_doctor;
        $doctor->tipo_documento = $request->tipo_documento;
        $doctor->telefono_doctor = $request->telefono_doctor;
        $doctor->direccion_doctor = $request->direccion_doctor;
        $doctor->especialidad = $request->especialidad;

        $doctor->save();

        return redirect()->route('doctores.index');

    }

